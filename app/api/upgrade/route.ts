import { NextRequest, NextResponse } from 'next/server';
import OpenAI from 'openai';
import { UpgradeRequest, UpgradeResponse } from '@/app/lib/types';

export const dynamic = 'force-dynamic';

export async function POST(req: NextRequest) {
  try {
    const body: UpgradeRequest = await req.json();

    if (!body.originalSolution || body.originalSolution.trim().length < 10) {
      return NextResponse.json({ error: 'Solution is too short' }, { status: 400 });
    }

    const apiKey = process.env.OPENROUTER_API_KEY;
    if (!apiKey) {
      return NextResponse.json(getMockUpgrade(body), { status: 200 });
    }

    const client = new OpenAI({
      baseURL: 'https://openrouter.ai/api/v1',
      apiKey,
      defaultHeaders: {
        'HTTP-Referer': 'https://case-trainer.app',
        'X-Title': 'CaseTrainer',
      },
    });
    const scoresText = Object.entries(body.feedback.scores)
      .map(([k, v]) => `${k}: ${v}/5`)
      .join(', ');

    const prompt = `Ты — Senior Product Manager и тренер по кейс-интервью с опытом в Google, Meta, Amazon, Avito и Uber.

Твоя задача — сначала оценить качество МЫШЛЕНИЯ студента, а затем переписать его ответ так, чтобы он звучал как ответ сильного PM-кандидата на интервью.

===КЕЙС===
Название: ${body.caseTitle}
Сложность: ${body.difficulty}
Тип / фокус: ${body.skillFocus.join(', ')}
Описание: ${body.caseDescription}

===ОТВЕТ СТУДЕНТА===
${body.originalSolution}

===ОЦЕНКИ ЗА ЭТОТ ОТВЕТ===
${scoresText}
Выявленные проблемы: ${body.feedback.issues.map((i) => `[${i.dimension}] ${i.issue}`).join('; ')}
Топ-3 улучшения: ${body.feedback.topFixes.join('; ')}

===ПОРЯДОК РАБОТЫ===

ШАГ 1 — ДИАГНОСТИКА МЫШЛЕНИЯ
Проанализируй ответ по 6 критериям и определи, где именно мышление слабое:
1. Problem Framing — правильно ли сформулирована суть проблемы?
2. Hypothesis Generation — есть ли осмысленные гипотезы с логикой причины и следствия?
3. Metrics Thinking — метрики используются логично, с конкретными числами и сегментацией?
4. Trade-off Reasoning — рассмотрены ли компромиссы и побочные эффекты решения?
5. Prioritization Logic — выбрано ли главное и объяснено почему именно это?
6. Structure & Communication — ответ логичен, структурирован, без воды?

ШАГ 2 — ПЕРЕПИСАТЬ INLINE КАК РЕДАКТОР
- Расплывчатые фразы → заменяй на конкретные с цифрами, метриками, терминами
- Поверхностные гипотезы → доводи до уровня «что именно проверяем, какой метрикой, и что считаем подтверждением»
- Пропущенные причинно-следственные связи → вставляй органично в нужное место
- Правильные мысли → оставляй, но формулируй чище и точнее
- Пропущенные блоки → добавляй в структуру, не снизу списком

ЗАПРЕЩЕНО:
- Копировать абзац студента без изменений и добавлять свой блок снизу
- Давать общую обратную связь без привязки к конкретному кейсу («подумай о метриках»)
- Фабриковать данные или выдумывать намерения студента

ШАГ 3 — COACHING QUESTIONS (режим совместного мышления)
Сформулируй 3 вопроса, которые помогут студенту самостоятельно дойти до правильного хода мысли на следующей итерации.
Хороший пример: «Какую метрику ты бы проверил первой, чтобы отделить проблему привлечения от проблемы активации?»
Плохой пример: «Подумай о метриках.»

ШАГ 4 — NEXT ITERATION TASK
Дай одно конкретное задание на следующую итерацию ответа — направленное на самое слабое место в рассуждении.

===ФОРМАТ ОТВЕТА — СТРОГО JSON===
{
  "upgradedSolution": "<переписанный ответ, markdown, PM-уровень мышления, голос студента сохранён>",
  "weaknesses": [
    "<аналитическая слабость 1 — конкретно, с привязкой к этому кейсу>",
    "<аналитическая слабость 2>",
    "<аналитическая слабость 3 — если есть>"
  ],
  "improvementExplanation": "<2-3 предложения: почему именно эти слабости критичны на интервью и что они говорят о ходе мысли>",
  "changes": [
    {
      "section": "<название блока>",
      "original": "<точная цитата из ответа студента — 1-2 предложения>",
      "improved": "<как конкретно переписана эта мысль>",
      "explanation": "<почему это сильнее на PM-интервью>"
    }
  ],
  "coachingQuestions": [
    "<вопрос 1 — конкретный, про этот кейс, развивает мышление>",
    "<вопрос 2>",
    "<вопрос 3>"
  ],
  "nextIterationTask": "<одно конкретное задание на следующую попытку>",
  "keyLessons": [
    "<урок 1 — конкретный вывод из этого ответа, без общих слов>",
    "<урок 2>",
    "<урок 3>"
  ]
}

weaknesses: 2-3 штуки. changes: 3-6 штук. coachingQuestions: ровно 3. keyLessons: ровно 3. В changes.original — цитируй точные слова студента.`;

    const completion = await client.chat.completions.create({
      model: process.env.OPENROUTER_MODEL ?? 'google/gemini-2.0-flash-001',
      temperature: 0.4,
      response_format: { type: 'json_object' },
      messages: [
        {
          role: 'system',
          content:
            'Ты Senior Product Manager и тренер по кейс-интервью. Ты ведёшь себя как строгий но конструктивный интервьюер из Google, Meta или Avito. Сначала оцениваешь качество мышления — выявляешь слабости в логике, метриках, причинно-следственных связях. Затем переписываешь ответ inline, сохраняя голос студента, но поднимая каждую мысль до уровня сильного PM-кандидата. Ты никогда не даёшь общую обратную связь — только конкретику, привязанную к этому кейсу. Отвечай строго в JSON.',
        },
        { role: 'user', content: prompt },
      ],
    });

    const raw = completion.choices[0].message.content ?? '{}';
    const parsed = JSON.parse(raw) as UpgradeResponse;

    if (!parsed.upgradedSolution || !parsed.changes) {
      throw new Error('Invalid AI response structure');
    }
    if (!parsed.weaknesses || parsed.weaknesses.length === 0) {
      parsed.weaknesses = ['Не выявлено конкретных слабостей — проверьте ответ вручную'];
    }
    if (!parsed.improvementExplanation) {
      parsed.improvementExplanation =
        'Ответ требует проработки аналитического слоя: конкретных метрик, проверяемых гипотез и причинно-следственных связей.';
    }
    if (!parsed.coachingQuestions || parsed.coachingQuestions.length === 0) {
      parsed.coachingQuestions = [
        'Какую метрику ты бы проверил первой, чтобы подтвердить главную гипотезу?',
        'Какие компромиссы создаёт твоё решение для других ключевых метрик продукта?',
        'Как ты расставил бы приоритеты между гипотезами, если бы мог проверить только одну за неделю?',
      ];
    }
    if (!parsed.nextIterationTask) {
      parsed.nextIterationTask =
        'Перепиши раздел с гипотезами: каждая должна содержать конкретную метрику для проверки и чёткий критерий подтверждения.';
    }
    if (!parsed.keyLessons || parsed.keyLessons.length === 0) {
      parsed.keyLessons = [
        'Структурируйте ответ по всем блокам даже если не уверены — пропуск блока виден сразу',
        'Конкретные метрики с цифрами сильнее общих слов о «росте» и «улучшении»',
        'Всегда называйте компромиссы решения — это признак зрелого продуктового мышления',
      ];
    }

    return NextResponse.json(parsed, { status: 200 });
  } catch (err) {
    console.error('[upgrade] error:', err);
    const message = err instanceof Error ? err.message : 'Unknown error';
    return NextResponse.json({ error: message }, { status: 500 });
  }
}

